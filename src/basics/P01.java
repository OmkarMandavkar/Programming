//WAP TO PRINT ALL THE NUMBERS FROM 1 TO 5
package basics;

public class P01 {

	public static void main(String[] args) {

		System.out.println("FOR LOOP");
		for(int i=1; i<=5; i++) {
			System.out.println(i);
		}

		System.out.println("\nDO WHILE LOOP");
		int x=1;
		do {
			System.out.println(x);
			x++;
		}
		while(x<=5);

	}

}
